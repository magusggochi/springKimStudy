package hello.hellospring.domain;

public class PromoCoupon {

	private String promoNo;
	private int doAmt;
	private int couponCnt;
	
	public String getPromoNo() {
		return promoNo;
	}
	public void setPromoNo(String promoNo) {
		this.promoNo = promoNo;
	}
	public int getDoAmt() {
		return doAmt;
	}
	public void setDoAmt(int doAmt) {
		this.doAmt = doAmt;
	}
	public int getCouponCnt() {
		return couponCnt;
	}
	public void setCouponCnt(int couponCnt) {
		this.couponCnt = couponCnt;
	}
	
}
